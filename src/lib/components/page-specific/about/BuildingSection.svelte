  <h2>Building from Source</h2>
  <h3>Prerequisites</h3>
  <p>
    Make sure you have <a href="https://nodejs.org/en/download/" target="_blank" rel="noopener noreferrer">Node.js</a>
    (v22 or higher) and <a href="https://git-scm.com/downloads" target="_blank" rel="noopener noreferrer">Git</a> installed
    on your machine.
  </p>
  <h3>Setup</h3>
  <div class="code">
    <code>git clone git@github.com:Lissy93/networking-toolbox.git</code>
    <code>cd networking-toolbox</code>
    <code>npm install</code>
    <code>npm start</code>
  </div>

  <h3>Dev</h3>
  <div class="code">
    <code>npm run dev</code>
  </div>

  <h3>Check 'n Test</h3>
  <div class="code">
    <code>npm run test</code>
    <code>npm run tsc</code>
    <code>npm run lint</code>
  </div>

  <h3>Deploying</h3>
  <ul>
    <li>
      <strong>Option #1 - Source:</strong><br />
      Run <code>npm run build</code>, and then upload the contents of <code>./dist</code>
      to any web server, CDN or static hosting provider
    </li>
    <li>
      <strong>Option #2 - Cloud:</strong><br />
      Just <a href="https://github.com/Lissy93/networking-toolbox/fork">fork the repo</a>, and import into Vercel,
      Netlify or any other cloud provider for instant deployment
    </li>
    <li>
      <strong>Option #3 - Docker:</strong><br />
      Build the Docker image, with <code>docker build -t networking-toolbox .</code>
      then run <code>docker run -p 8080:8080 networking-toolbox</code>
    </li>
  </ul>

